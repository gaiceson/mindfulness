export const getRecommendedSessions = (emotion: EmotionType): MeditationSession[] => {
  const map: Record<EmotionType, SessionCategory[]> = {
    great: ['morning', 'love', 'focus'],
    good: ['focus', 'morning', 'breath'],
    neutral: ['breath', 'stress', 'focus'],
    tired: ['sleep', 'stress', 'breath'],
    anxious: ['anxiety', 'breath', 'stress'],
  };
  const cats = map[emotion];
  return SESSIONS.filter(s => cats.includes(s.category)).slice(0, 4);
};
